/*
**For you to do:**
Create a do while loop that prints out "Hello World!" 5 times.
**Output:**
Hello World!
Hello World!
Hello World!
Hello World!
Hello World!
 */
public class Repl52 {
    public static void main(String[] args) {

        int i=0;
        do {
            System.out.println("Hello World!");
            i++;
        }while (i<5);


    }
}
